const SUPPORTED_LANGS = ['en', 'vi', 'fr'];

function normalizeLang(lang) {
  const raw = (lang || '').toString().toLowerCase().trim();
  return SUPPORTED_LANGS.includes(raw) ? raw : 'en';
}

function getTranslator(lang) {
  const safeLang = normalizeLang(lang);
  return function t(key, fallback) {
    const parts = (key || '').toString().split('.').filter(Boolean);
    let obj = translations[safeLang];
    for (const p of parts) {
      if (!obj || typeof obj !== 'object') {
        obj = undefined;
        break;
      }
      obj = obj[p];
    }

    if (obj === undefined) {
      if (fallback !== undefined) return fallback;

      obj = translations.en;
      for (const p of parts) {
        if (!obj || typeof obj !== 'object') {
          obj = undefined;
          break;
        }
        obj = obj[p];
      }
    }

    return obj !== undefined ? obj : (fallback !== undefined ? fallback : key);
  };
}

